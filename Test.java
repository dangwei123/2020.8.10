class Solution {
    public int countBinarySubstrings(String s) {
        int ret=0;
        int pre=0;
        int cur=0;
        while(cur<s.length()){
            char c=s.charAt(cur);
            int num=0;
            while(cur<s.length()&&s.charAt(cur)==c){
                cur++;
                num++;
            }
            ret+=Math.min(pre,num);
            pre=num;
        }
        return ret;
    }
}